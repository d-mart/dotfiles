# add support for ctrl+o to open selected file in editor
export FZF_DEFAULT_OPTS="--bind='ctrl-o:execute($EDITOR {})+abort'"

# fh - repeat history
fh() {
  print -z $( ([ -n "$ZSH_NAME" ] && fc -l 1 || history) | fzf +s --tac | sed 's/ *[0-9]* *//')
}

